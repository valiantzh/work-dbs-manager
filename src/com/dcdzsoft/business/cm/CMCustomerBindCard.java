public class CMCustomerBindCard extends ActionBean
{

    public int doBusiness(InParamCMCustomerBindCard in) throws EduException
    {
        utilDAO = this.getUtilDAO();
        commonDAO = this.getCommonDAO();
        dbSession = this.getCurrentDBSession();
        int result = 0;

		//1.验证输入参数是否有效，如果无效返回-1。
		if (StringUtils.isEmpty(in.CustomerID)
			||StringUtils.isEmpty(in.BindCardID))
			throw new EduException(ErrorCode.ERR_PARMERR);




		//3.调用UtilDAO.getCurrentDateTime()获得系统日期时间。
		java.sql.Timestamp sysDateTime = utilDAO.getCurrentDateTime();
		java.sql.Date sysDate = DateUtils.toSQLDate(sysDateTime);


		//4.调用CommonDAO.addOperatorLog(OperID，功能编号，”修改控制参数信息”,系统日期时间，“”)。
		CommonDAO mmonDAO = daoFactory.getCommonDAO();



		// 调用CommonDAO.addOperatorLog(OperID，功能编号，系统日期时间，“”)
		OPOperatorLog log = new OPOperatorLog();
		log.OperID = in.CustomerID;
		log.FunctionID = in.getFunctionID();
		log.OccurTime = sysDateTime;
		log.StationAddr = "";
		log.Remark = "";

		commonDAO.addOperatorLog(log);


        return result;
    }
}
