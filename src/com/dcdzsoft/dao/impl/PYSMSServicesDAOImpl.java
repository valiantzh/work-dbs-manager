	public PYSMSServices find(PYSMSServices obj) throws EduException
	{
		PYSMSServices result = null;
		PreparedStatement pstmt = null;
		ResultSet rset = null;

		String sql = "SELECT ServiceID,ServiceName,CreateTime,Active,Ways,Amount,Price,SMSNum,Units,LastModifyTime,Remark  FROM PYSMSServices WHERE ServiceID = ? ";

		log.debug("[find sql]:" + "SELECT ServiceID,ServiceName,CreateTime,Active,Ways,Amount,Price,SMSNum,Units,LastModifyTime,Remark  FROM PYSMSServices WHERE ServiceID = " + StringUtils.addQuote(obj.ServiceID) );

		try
		{
			pstmt = conn.prepareStatement(sql);
			PreparedStmtUtils.setString(pstmt,1,obj.ServiceID);
			rset = pstmt.executeQuery();

			if(rset.next() == true)
			{
				result = new PYSMSServices();
				result.ServiceID = rset.getString(1);
				result.ServiceName = rset.getString(2);
				result.CreateTime = rset.getTimestamp(3);
				result.Active = rset.getString(4);
				result.Ways = rset.getString(5);
				result.Amount = rset.getDouble(6);
				result.Price = rset.getDouble(7);
				result.SMSNum = rset.getLong(8);
				result.Units = rset.getString(9);
				result.LastModifyTime = rset.getTimestamp(10);
				result.Remark = rset.getString(11);

			}
			else
			{
				throw new EduException(ErrorCode.ERR_PYSMSSERVICESNODATA);
			}
		}
		catch(SQLException e)
		{
			String errorCode = ErrorCode.ERR_QRYPYSMSSERVICESFAIL;
			log.error("[DBERROR-("+ errorCode + ")]==>[sqlcode]:"+e.getErrorCode() + " [errmsg]:"+e.getMessage() + " [sql]:"+sql);
			throw new EduException(errorCode);
		}
		finally
		{
			dbSession.closeResultSet(rset);
			dbSession.closeStatement(pstmt);
		}

		return result;
	}


