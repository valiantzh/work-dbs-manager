		String sql = "SELECT CardNo,CardType,CardStatus,Balance,BalanceUnit,CustomerID,CreditLimit,Discount,EffectiveDate,ExpirationDate,WarningLimit,WarningFlag,CreateTime,LastModifyTime,Remark  FROM PYConsumedCard WHERE CardNo = ? ";

