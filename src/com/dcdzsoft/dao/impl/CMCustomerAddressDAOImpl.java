	public CMCustomerAddress find(CMCustomerAddress obj) throws EduException
	{
		CMCustomerAddress result = null;
		PreparedStatement pstmt = null;
		ResultSet rset = null;

		String sql = "SELECT CustomerID,AddressID,Address,DefaultFLag,TerminalNo,LastModifyTime,Remark  FROM CMCustomerAddress WHERE CustomerID = ?  AND AddressID = ? ";

		log.debug("[find sql]:" + "SELECT CustomerID,AddressID,Address,DefaultFLag,TerminalNo,LastModifyTime,Remark  FROM CMCustomerAddress WHERE CustomerID = " + StringUtils.addQuote(obj.CustomerID) +
						" AND AddressID = " + StringUtils.addQuote(obj.AddressID) );

		try
		{
			pstmt = conn.prepareStatement(sql);
			PreparedStmtUtils.setString(pstmt,1,obj.CustomerID);
			PreparedStmtUtils.setString(pstmt,2,obj.AddressID);
			rset = pstmt.executeQuery();

			if(rset.next() == true)
			{
				result = new CMCustomerAddress();
				result.CustomerID = rset.getString(1);
				result.AddressID = rset.getString(2);
				result.Address = rset.getString(3);
				result.DefaultFLag = rset.getString(4);
				result.TerminalNo = rset.getString(5);
				result.LastModifyTime = rset.getTimestamp(6);
				result.Remark = rset.getString(7);

			}
			else
			{
				throw new EduException(ErrorCode.ERR_CMCUSTOMERADDRESSNODATA);
			}
		}
		catch(SQLException e)
		{
			String errorCode = ErrorCode.ERR_QRYCMCUSTOMERADDRESSFAIL;
			log.error("[DBERROR-("+ errorCode + ")]==>[sqlcode]:"+e.getErrorCode() + " [errmsg]:"+e.getMessage() + " [sql]:"+sql);
			throw new EduException(errorCode);
		}
		finally
		{
			dbSession.closeResultSet(rset);
			dbSession.closeStatement(pstmt);
		}

		return result;
	}


