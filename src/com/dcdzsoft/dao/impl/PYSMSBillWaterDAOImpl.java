		String sql = "SELECT AccountID,BillWaterID,BillTime,BillType,Ways,Amount,BillNum,Units,SMSNum,SMSBalance,ServiceID,Remark  FROM PYSMSBillWater WHERE AccountID = ?  AND BillWaterID = ? ";

