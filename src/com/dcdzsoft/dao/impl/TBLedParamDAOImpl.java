	public TBLedParam find(TBLedParam obj) throws EduException
	{
		TBLedParam result = null;
		PreparedStatement pstmt = null;
		ResultSet rset = null;

		String sql = "SELECT TerminalNo,LedMsg,StartPointX,StartPointY,LedWidth,LedHeight,DisplayWay,QuitWay,RunSpeed,StopTime,FontSize,FontColor,LastModifyTime,Remark  FROM TBLedParam WHERE TerminalNo = ? ";

		log.debug("[find sql]:" + "SELECT TerminalNo,LedMsg,StartPointX,StartPointY,LedWidth,LedHeight,DisplayWay,QuitWay,RunSpeed,StopTime,FontSize,FontColor,LastModifyTime,Remark  FROM TBLedParam WHERE TerminalNo = " + StringUtils.addQuote(obj.TerminalNo) );

		try
		{
			pstmt = conn.prepareStatement(sql);
			PreparedStmtUtils.setString(pstmt,1,obj.TerminalNo);
			rset = pstmt.executeQuery();

			if(rset.next() == true)
			{
				result = new TBLedParam();
				result.TerminalNo = rset.getString(1);
				result.LedMsg = rset.getString(2);
				result.StartPointX = rset.getInt(3);
				result.StartPointY = rset.getInt(4);
				result.LedWidth = rset.getInt(5);
				result.LedHeight = rset.getInt(6);
				result.DisplayWay = rset.getInt(7);
				result.QuitWay = rset.getInt(8);
				result.RunSpeed = rset.getInt(9);
				result.StopTime = rset.getInt(10);
				result.FontSize = rset.getInt(11);
				result.FontColor = rset.getInt(12);
				result.LastModifyTime = rset.getTimestamp(13);
				result.Remark = rset.getString(14);

			}
			else
			{
				throw new EduException(ErrorCode.ERR_TBLEDPARAMNODATA);
			}
		}
		catch(SQLException e)
		{
			String errorCode = ErrorCode.ERR_QRYTBLEDPARAMFAIL;
			log.error("[DBERROR-("+ errorCode + ")]==>[sqlcode]:"+e.getErrorCode() + " [errmsg]:"+e.getMessage() + " [sql]:"+sql);
			throw new EduException(errorCode);
		}
		finally
		{
			dbSession.closeResultSet(rset);
			dbSession.closeStatement(pstmt);
		}

		return result;
	}


