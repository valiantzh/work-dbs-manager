		ResultSet rset = null;

		String sql = "SELECT WaterID,TerminalNo,CustomerMobile,PackageID,PostmanID,StoredDate,StoredTime,ReminderNum,LastModifyTime,Remark  FROM MBReminderWater WHERE WaterID = ? ";

