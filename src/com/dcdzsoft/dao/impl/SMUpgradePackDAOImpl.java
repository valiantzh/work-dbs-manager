		SMUpgradePack result = null;
		PreparedStatement pstmt = null;
		ResultSet rset = null;

		String sql = "SELECT SoftwareID,SystemID,SoftwareName,SoftwareType,SoftwareVersion,SoftSignMd5,SystemType,DownloadUrl,UpdateContent,LastModifyTime,Remark  FROM SMUpgradePack WHERE SoftwareID = ? ";

