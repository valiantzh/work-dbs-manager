	public MBSignInfo find(MBSignInfo obj) throws EduException
	{
		MBSignInfo result = null;
		PreparedStatement pstmt = null;
		ResultSet rset = null;

		String sql = "SELECT TerminalNo,SignTime,SignIP,SignMac,SignKey,SoftwareVersion,InitPasswd,OnlineStatus,LastModifyTime,Remark  FROM MBSignInfo WHERE TerminalNo = ? ";

		log.debug("[find sql]:" + "SELECT TerminalNo,SignTime,SignIP,SignMac,SignKey,SoftwareVersion,InitPasswd,OnlineStatus,LastModifyTime,Remark  FROM MBSignInfo WHERE TerminalNo = " + StringUtils.addQuote(obj.TerminalNo) );

		try
		{
			pstmt = conn.prepareStatement(sql);
			PreparedStmtUtils.setString(pstmt,1,obj.TerminalNo);
			rset = pstmt.executeQuery();

			if(rset.next() == true)
			{
				result = new MBSignInfo();
				result.TerminalNo = rset.getString(1);
				result.SignTime = rset.getTimestamp(2);
				result.SignIP = rset.getString(3);
				result.SignMac = rset.getString(4);
				result.SignKey = rset.getString(5);
				result.SoftwareVersion = rset.getString(6);
				result.InitPasswd = rset.getString(7);
				result.OnlineStatus = rset.getString(8);
				result.LastModifyTime = rset.getTimestamp(9);
				result.Remark = rset.getString(10);

			}
			else
			{
				throw new EduException(ErrorCode.ERR_MBSIGNINFONODATA);
			}
		}
		catch(SQLException e)
		{
			String errorCode = ErrorCode.ERR_QRYMBSIGNINFOFAIL;
			log.error("[DBERROR-("+ errorCode + ")]==>[sqlcode]:"+e.getErrorCode() + " [errmsg]:"+e.getMessage() + " [sql]:"+sql);
			throw new EduException(errorCode);
		}
		finally
		{
			dbSession.closeResultSet(rset);
			dbSession.closeStatement(pstmt);
		}

		return result;
	}


