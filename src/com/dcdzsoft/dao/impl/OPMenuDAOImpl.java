	public OPMenu find(OPMenu obj) throws EduException
	{
		OPMenu result = null;
		PreparedStatement pstmt = null;
		ResultSet rset = null;

		String sql = "SELECT MenuID,MenuName,MenuLevel,MenuEngName,CorpShortName,Description,Action,HotKey,Icon,HelpContext,MenuType,LeafFlag,Remark  FROM OPMenu WHERE MenuID = ? ";

		log.debug("[find sql]:" + "SELECT MenuID,MenuName,MenuLevel,MenuEngName,CorpShortName,Description,Action,HotKey,Icon,HelpContext,MenuType,LeafFlag,Remark  FROM OPMenu WHERE MenuID = " + StringUtils.addQuote(obj.MenuID) );

		try
		{
			pstmt = conn.prepareStatement(sql);
			PreparedStmtUtils.setString(pstmt,1,obj.MenuID);
			rset = pstmt.executeQuery();

			if(rset.next() == true)
			{
				result = new OPMenu();
				result.MenuID = rset.getString(1);
				result.MenuName = rset.getString(2);
				result.MenuLevel = rset.getInt(3);
				result.MenuEngName = rset.getString(4);
				result.CorpShortName = rset.getString(5);
				result.Description = rset.getString(6);
				result.Action = rset.getString(7);
				result.HotKey = rset.getInt(8);
				result.Icon = rset.getString(9);
				result.HelpContext = rset.getString(10);
				result.MenuType = rset.getInt(11);
				result.LeafFlag = rset.getInt(12);
				result.Remark = rset.getString(13);

			}
			else
			{
				throw new EduException(ErrorCode.ERR_OPMENUNODATA);
			}
		}
		catch(SQLException e)
		{
			String errorCode = ErrorCode.ERR_QRYOPMENUFAIL;
			log.error("[DBERROR-("+ errorCode + ")]==>[sqlcode]:"+e.getErrorCode() + " [errmsg]:"+e.getMessage() + " [sql]:"+sql);
			throw new EduException(errorCode);
		}
		finally
		{
			dbSession.closeResultSet(rset);
			dbSession.closeStatement(pstmt);
		}

		return result;
	}


